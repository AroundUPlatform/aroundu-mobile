def apply_translations():
    """Apply all translations to the locale ARB files."""
    en_path = os.path.join(ARB_DIR, 'app_en.arb')
    with open(en_path, 'r', encoding='utf-8') as f:
        en = json.load(f)

    en_keys = {k: v for k, v in en.items()
               if not k.startswith('@') and not k.startswith('@@')}

    for arb_path in sorted(glob.glob(os.path.join(ARB_DIR, 'app_*.arb'))):
        locale = os.path.basename(arb_path).replace('app_', '').replace('.arb', '')
        if locale == 'en':
            continue

        with open(arb_path, 'r', encoding='utf-8') as f:
            data = json.load(f)

        trans = TRANSLATIONS.get(locale, {})
        updated = 0

        for key, en_val in en_keys.items():
            # Add missing keys
            if key not in data:
                data[key] = trans.get(key, en_val)
                updated += 1
            # Replace keys that are still English
            elif data[key] == en_val and key in trans:
                data[key] = trans[key]
                updated += 1

        with open(arb_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')

        print(f'{locale:6s}: updated {updated} keys')

    print('\nDone! Run `flutter gen-l10n` to regenerate.')


if __name__ == '__main__':
    apply_translations()
